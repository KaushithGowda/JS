//Logical operators

//AND Operator(&&)
let day = "monday";
let money = 55;

if(day === "friday" && money > 50 ){
    
    console.log("You are going out");
}
else{

    console.log("You are not going out");
}


//OR Operator(||)
let day2 = "monday";
let money2 = 55;

if(day2 === "friday" || money2 > 50 ){
    
    console.log("You are going out");
}
else{

    console.log("You are not going out");
}
